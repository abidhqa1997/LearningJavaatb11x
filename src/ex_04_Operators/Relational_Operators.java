package ex_04_Operators;

public class Relational_Operators {
    public static void main(String[] args) {
        int a = 10;
        int b = 30;
        boolean c = a > b; // // 10> 30
        System.out.println(c);

        int age_Likhitha = 33;
        int age_Likhita = 34;
        boolean result = age_Likhita >= age_Likhitha;
        System.out.println(result);
    }
}
