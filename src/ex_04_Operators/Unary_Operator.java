package ex_04_Operators;

public class Unary_Operator {
    public static void main(String[] args) {
        int a=50;
        int a1=-20;
        int result=a+a1;
        int result2=a-a1;
        int result3=a1-a;
        System.out.println(a);
        System.out.println(result);
        System.out.println(result2);
        System.out.println(result3);
    }
}
